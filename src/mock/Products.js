// Obtain all the products from Side Tables List
export const getAllProducts = (idCategory) => {
    return new Promise ((resolve) => {
        const productsToResolve = idCategory ? products.filter(item => item.category === idCategory) : products
        setTimeout(() => {
            resolve(productsToResolve);
        }, 2000);
    });
}

// Obtain just one product from Side Tables list
export const getSingleProduct = (id) => {
    return new Promise ((resolve) => {
        const prod= products.find (p => p.id === parseInt(id))
        setTimeout(() => {
            resolve(prod)
        }, 2000);
    });
};

